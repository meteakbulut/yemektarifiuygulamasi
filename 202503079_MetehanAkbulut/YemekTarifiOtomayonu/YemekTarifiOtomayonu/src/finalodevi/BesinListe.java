package finalodevi;

public class BesinListe {
	private int bID;
	private String bAd;
	private String bTipi;
	private Double bKalori;
	private int bStok;
	
	
	public int getbID() {
		return bID;
	}
	public void setbID(int bID) {
		this.bID = bID;
	}
	public String getbAd() {
		return bAd;
	}
	public void setbAd(String bAd) {
		this.bAd = bAd;
	}
	public String getbTipi() {
		return bTipi;
	}
	public void setbTipi(String bTipi) {
		this.bTipi = bTipi;
	}
	public Double getbKalori() {
		return bKalori;
	}
	public void setbKalori(Double bKalori) {
		this.bKalori = bKalori;
	}
	public int getbStok() {
		return bStok;
	}
	public void setbStok(int bStok) {
		this.bStok = bStok;
	}
	
	
}
